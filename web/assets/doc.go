// The assets package provides assets for use in the web application.
//
// In a production build the assets are treated as read-only and are directly
// compiled into the application binary.  During development, assets are
// reloaded from disk each time they are requested.  This allows development to
// not require constant starting and stopping of the application just to change
// an asset.
//
// Currently the "web/static" and "web/templates" directories are treated as
// asset directories and any files present in those directories are available to
// the application at runtime.
package assets
